    private void botao_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_editarActionPerformed
        //Começa de 0 -1 não existe nenhuma linha selecionada
        int linhaSelecionada = -1;
        linhaSelecionada = tabela.getSelectedRow();
       // System.exit(0);
        if (linhaSelecionada >= 0) {
            Integer codigoCargoSelecionado = (Integer) tabela.getValueAt(linhaSelecionada, 0);
        
            CargoGUI f = new CargoGUI(modelo, linhaSelecionada, codigoCargoSelecionado);
            f.setVisible(true);

        } else {
            JOptionPane.showMessageDialog(null, "Nenhuma linha selecionada!");
        }
    }//GEN-LAST:event_botao_editarActionPerformed

