    private void botao_removerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_removerActionPerformed

        int linhaSelecionada = tabela.getSelectedRow();
        Integer codigoCargoSelecionado = (Integer) tabela.getValueAt(linhaSelecionada, 0);
        Cargo c = new Cargo();
        c.setId(codigoCargoSelecionado);

        int resposta = JOptionPane.showConfirmDialog(null, "Você deseja realmente excluir este registro?", "", JOptionPane.YES_NO_OPTION);
        if (linhaSelecionada >= 0) {
            if (resposta == 0) {
                CargoController cc = new CargoController();
                if (cc.remove(c) == true) {
                    modelo.removeRow(linhaSelecionada);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Nenhuma linha selecionada!");
        }

    }//GEN-LAST:event_botao_removerActionPerformed

